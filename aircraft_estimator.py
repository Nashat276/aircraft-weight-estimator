def run_sizing(p):
    # Fixed weight fractions (Raymer standard)
    # W1/Wto (Warmup/Taxi), W2/W1 (Takeoff), W3/W2 (Climb), W7/W6 (Landing)
    # W4/W3 (Cruise), W5/W4 (Loiter) - Calculated via Breguet
    
    # Payload & Crew
    W_pl = p['npax'] * (p['wpax'] + p['wbag']) + p['ncrew'] * 205 + p['natt'] * 200
    W_crew = p['ncrew'] * 205 + p['natt'] * 200
    
    # Breguet Cruise Fraction
    R_statute = p['R'] * 1.15078
    Wc = 1.0 / math.exp(R_statute / (375.0 * (p['npc'] / p['Cpc']) * p['LDc']))
    
    # Breguet Loiter Fraction
    V_mph = p['Vl'] * 1.15078
    Wl = 1.0 / math.exp(p['El'] / (375.0 * (1.0 / V_mph) * (p['npl'] / p['Cpl']) * p['LDl']))
    
    # Mission Fuel Fraction (Mff)
    # Start: 0.990, Taxi: 0.995, Takeoff: 0.995, Climb: 0.985, Descent: 0.985, Landing: 0.995
    Mff = 0.990 * 0.995 * 0.995 * 0.985 * Wc * Wl * 0.985 * 0.995
    
    # Weights using User-Inputted W_TO
    Wto = p['Wto_user']
    Wtfo = Wto * p['Mtfo']
